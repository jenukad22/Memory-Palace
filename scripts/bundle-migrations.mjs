import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const migDir = join(root, 'src', 'db', 'migrations');

const migrations = readdirSync(migDir)
  .filter((f) => f.endsWith('.sql'))
  .sort() // zero-padded numeric prefixes sort correctly
  .map((file) => {
    const raw = readFileSync(join(migDir, file), 'utf8');
    const statements = raw
      .split('--> statement-breakpoint')
      .map((s) => s.trim())
      .filter(Boolean);
    return { tag: file.replace(/\.sql$/, ''), statements };
  });

const out = `// AUTO-GENERATED by scripts/bundle-migrations.mjs — do not edit.
export interface Migration {
  tag: string;
  statements: string[];
}

export const MIGRATIONS: Migration[] = ${JSON.stringify(migrations, null, 2)};
`;

writeFileSync(join(migDir, '..', 'migrations.generated.ts'), out);
console.log(`Bundled ${migrations.length} migration(s): ${migrations.map((m) => m.tag).join(', ')}`);
